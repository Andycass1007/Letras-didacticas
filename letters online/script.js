const quizContainer = document.getElementById('quiz-container');
const startContainer = document.querySelector('.start-container');
const playerNameInput = document.getElementById('playerName');
const questionNumberElement = document.getElementById('question-number');
const questionTextElement = document.getElementById('question-text');
const optionsContainer = document.getElementById('options-container');
const timerElement = document.getElementById('timer');
const startButton = document.getElementById('startButton');
const nextButton = document.getElementById('nextButton');
const restartButton = document.getElementById('restartButton');
const returnButton = document.getElementById('returnButton');
const AudioMillonario = document.getElementById("MillonarioSound");


let currentQuestion = 0;
let score = 0;
let timer;
let timeLeft = 300;

startButton.addEventListener('click', startQuiz);
nextButton.addEventListener('click', loadNextQuestion);
restartButton.addEventListener('click', startQuiz);
returnButton.addEventListener('click', returnToMainPage);

function startQuiz() {
    const playerName = playerNameInput.value.trim();
    if (playerName === '') {
        alert('Por favor, ingresa tu nombre.');
        return;
    }

    startContainer.style.display = 'none';
    quizContainer.style.display = 'block';
    loadQuestion(0);
    startTimer();
 AudioMillonario.play;
}

function startTimer() {
    timer = setInterval(function() {
        if (timeLeft > 0) {
            timeLeft--;
            timerElement.textContent = timeLeft;
        } else {
            endQuiz();
        }
    }, 1000);
 AudioMillonario.play;
}

function loadQuestion(index) {
    if (index >= questions.length) {
        endQuiz();
        return;
    }

    const question = questions[index];
    questionNumberElement.textContent = index + 1;
    questionTextElement.textContent = question.question;
    optionsContainer.innerHTML = '';

    question.options.forEach((option, i) => {
        const button = document.createElement('button');
        button.textContent = option;
        button.addEventListener('click', () => checkAnswer(index, i));
        optionsContainer.appendChild(button);
    });
}

function checkAnswer(questionIndex, optionIndex) {
    if (questions[questionIndex].answer === optionIndex) {
        score++;
    }

    loadQuestion(questionIndex + 1);
}

function loadNextQuestion() {
    currentQuestion++;
    loadQuestion(currentQuestion);
}

function endQuiz() {
    clearInterval(timer);
    quizContainer.style.display = 'none';
    restartButton.style.display = 'inline-block';
    returnButton.style.display = 'inline-block';
    document.getElementById('nixtButton').style.display = 'inline-block';
    alert(`¡Juego terminado! ${playerNameInput.value} acertaste ${score} de 30 preguntas.`);
 AudioMillonario.play;
}

function returnToMainPage() {
    location.reload();
 AudioMillonario.play;
}

